console.log(`primitives`);

//STRING
let stringData = 'string data';
console.log(stringData);

console.log(typeof stringData);

stringData = '';
console.log(stringData);
console.log(typeof stringData);

stringData = ' ';
console.log(stringData);
console.log(typeof stringData);

//NUMBER
let number = 1;
console.log(number);
console.log(typeof number);

number = 2e53;
console.log(number);
console.log(typeof number);

number = Infinity;
console.log(number);
console.log(typeof number);

number = NaN; //not a number
console.log(number);
console.log(typeof number);

//BOOLEAN true || false
let isOnline = true;
console.log(isOnline);
console.log(typeof isOnline);

isOnline = false;
console.log(isOnline);
console.log(typeof isOnline);

//UNDEFINED
let massage;
console.log(massage);
console.log(typeof massage);

//NULL
let user = null;
console.log(user);
console.log(typeof user);